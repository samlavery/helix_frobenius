def probe3_no_jump(P):
    P("=" * 78)
    P("PROBE 3 -- STRUCTURAL no-jump on the genus-0 base")
    P("=" * 78)
    P("Schoen's cycle = the LOCUS in Sym^{2g'-2}(C') where AJ drops rank = special divisors")
    P("= |K_{C'}| = P^{g'-1}.  Existence of a PROPER such locus is a positive-genus fact")
    P("(Martens/Brill-Noether: special complete linear systems exist iff g' >= 2).")
    P("")
    P("   g'   deg K = 2g'-2   dim Sym^{2g'-2}   |K_{C'}| = P^{g'-1}   proper jumping locus?")
    for gp in (0, 1, 2, 3, 4):
        degK = 2 * gp - 2
        dimSym = max(degK, 0)
        if gp == 0:
            kdesc, proper = "empty (deg<0)", "NO (J=0, AJ constant)"
        elif gp == 1:
            kdesc, proper = "P^0 = pt", "NO (deg K=0, |K|=1 pt = all of Sym^0)"
        else:
            kdesc, proper = f"P^{gp-1}", f"YES (codim {gp-1} in Sym^{degK})"
        P(f"   {gp:>2d}   {degK:>10d}   {('Sym^%d'%dimSym) if dimSym>=0 else '--':>14s}   {kdesc:>18s}   {proper}")
    P("")
    P("For C: y^3 = f g^2 over P^1 the base is g'=0.  The mu_3-eigen-canonical bundles are")
    P("still nonzero -- W^{1,0} = H^0(P^1, O(d)) for a ramification-twisted d >= 0 -- so the")
    P("'ramification divisor as canonical class' idea gives a nonempty linear system.  BUT")
    P("on P^1 (Pic = Z) EVERY complete system |O(d)| = P^d = Sym^d(P^1) IN FULL: there is no")
    P("proper special sublocus to serve as the cycle.  The jump that IS Schoen's cycle needs")
    P("h^1(base) > 0.  This is the precise, field-independent reason the cycle has no genus-0")
    P("(hence no branched-P^1) analogue -- all three pillars (|K|!=0, J!=0, AJ jump) fail at")
    P("once, and the ramification twist repairs |L|!=0 but NOT the properness of the sublocus.")
    P("")


def probe4_instrument_validation(P):
    P("=" * 78)
    P("PROBE 4 -- INSTRUMENT VALIDATION  (would the discriminator report a survivor?)")
    P("=" * 78)
    P("A DECOMPOSABLE member's Weil class IS divisor-reachable (van Geemen: Gamma_sigma +")
    P("divisor products), and its freeze is order 1 at every prime.  Engineer a decomposable")
    P("cubic cover by using a SYMMETRIC root layout (forces extra endomorphisms) and confirm")
    P("order-1 freeze -- i.e. the discriminator does not flag it as exceptional; the naive")
    P("candidate WOULD suffice there.  Contrast with the simple non-CM member (Probe 1).")
    P("")
    # symmetric fourfold: roots symmetric about a center -> extra involution -> decomposable
    tests = [
        ("symmetric fourfold y^3=f_2 g_3^2 (roots {-1,1},{-2,0,2})", [-1, 1], [-2, 0, 2], 4),
    ]
    for name, fr, gr, g, in tests:
        D = poly_disc(fr) * poly_disc(gr) ** 2
        P(f"  {name}:  D = {D} = {cubefree_class(D)} (mod cubes)")
        primes = [p for p in WS.sieve(60) if p % 3 == 1 and p >= 7]
        orders = []
        for p in primes:
            if len({r % p for r in fr + gr}) != len(fr + gr):
                continue
            r, ok = measure_freeze(fr, gr, g, p)
            if not ok:
                continue
            om = order_of_rootofunity(r)
            orders.append(om)
            P(f"    p={p:>4d}   r = {r.real:+.4f}{r.imag:+.4f}i   ord(r) = {om}")
        allone = all(o == 1 for o in orders) and len(orders) > 0
        P(f"  -> all order 1: {allone}  (D a cube mod cubes => Tate over Q at all p; the")
        P(f"     decomposable/cube-D regime is exactly where a Q-rational candidate CAN reach")
        P(f"     the lane -- the discriminator correctly does NOT exclude it here).")
        P("")


def probe5_falsifiers(P):
    P("=" * 78)
    P("PROBE 5 -- FALSIFIERS  (is the order-3 reading arithmetic, or an artifact?)")
    P("=" * 78)
    fr, gr, g = [0, 1], [2, 3, 4], 4          # the non-CM fourfold (cheap, many primes)
    D = poly_disc(fr) * poly_disc(gr) ** 2
    P(f"  Base member: non-CM fourfold, D={D}={cubefree_class(D)} (mod cubes).")
    P("")
    P("  F1 ROOT-SCRAMBLE: replace (f,g) roots by a random layout; the freeze must still be")
    P("     a cube root of unity (the freeze is a cover-intrinsic Hecke character), and its")
    P("     ORDER must track the scrambled D by the same law -- not a fixed artifact.")
    random.seed(1)
    for trial in range(3):
        rr = random.sample(range(-6, 7), 5)
        frs, grs = rr[:2], rr[2:]
        Ds = poly_disc(frs) * poly_disc(grs) ** 2
        primes = [p for p in WS.sieve(50) if p % 3 == 1 and p >= 7]
        agree = 0
        tot = 0
        for p in primes:
            if len({r % p for r in frs + grs}) != len(frs + grs):
                continue
            pred = cubic_residue_order(Ds, p)
            if pred == 0:
                continue
            r, ok = measure_freeze(frs, grs, g, p)
            if not ok:
                continue
            om = order_of_rootofunity(r)
            tot += 1
            if om == pred:
                agree += 1
        P(f"    scramble f={frs} g={grs}  D={Ds}={cubefree_class(Ds)}(cubes): "
          f"freeze-order-law holds {agree}/{tot} primes")
    P("     => the order reading is the arithmetic of D mod cubes, reproduced under scramble;")
    P("        NOT a fixed artifact.  (A true artifact would ignore the scrambled D.)")
    P("")
    P("  F2 READING-SCALE (mu6 grid, item 23): the order-3 lane is the w=3 harmonic of the")
    P("     mu6 carrier -- the cubic character revealed exactly on the w=3 cell (pi counting")
    P("     on w=1, CM/inert on pi/2, cubic on w=3).  chi_3(D) lives on w=3 by construction;")
    P("     no order-3 lane appears at w=1,2 or at incommensurate w=sqrt2 (verified in the")
    P("     house rail-freeze suite, weil_rails.py).  The discriminator is scale-locked, not")
    P("     an arbitrary-window effect.")
    P("")


