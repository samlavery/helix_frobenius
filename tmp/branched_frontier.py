# exact discriminant / freeze-order-law helpers  (item 27/37)
# ---------------------------------------------------------------------------
def poly_disc(roots):
    """discriminant of monic prod(x - r) with integer roots = prod_{i<j}(r_i - r_j)^2."""
    d = 1
    for i in range(len(roots)):
        for j in range(i + 1, len(roots)):
            d *= (roots[i] - roots[j]) ** 2
    return d


def cubefree_class(n):
    """n mod cubes: strip perfect-cube factors, return the cube-free representative."""
    n = abs(n)
    out = 1
    d = 2
    while d * d * d <= n:
        while n % (d ** 3) == 0:
            n //= d ** 3
        d += 1
    # now collect residual prime powers mod 3
    out = 1
    d = 2
    m = n
    while d * d <= m:
        e = 0
        while m % d == 0:
            m //= d
            e += 1
        e %= 3
        out *= d ** e
        d += 1
    if m > 1:
        out *= m
    return out


def cubic_residue_order(D, p):
    """order of chi_3(D) mod p (p = 1 mod 3):  1 if D is a cube mod p, else 3.
       (0 if p | D -- ramified/bad.)"""
    if D % p == 0:
        return 0
    r = pow(D % p, (p - 1) // 3, p)
    return 1 if r == 1 else 3


def freeze_order_predicted(fr, gr, p):
    """predicted ord(r) from the freeze-order law:  ord chi_3(disc(f) disc(g)^2)."""
    D = poly_disc(fr) * poly_disc(gr) ** 2
    return cubic_residue_order(D, p), D


# ---------------------------------------------------------------------------
# measured Weil freeze from point counts (reuse the sixfold instrument)
# ---------------------------------------------------------------------------
def measure_freeze(fr, gr, g, p):
    """returns (r, ok) with r = det(Frob|V_zeta)/p^{g/2} the Weil freeze scalar, or None."""
    allroots = fr + gr
    if (p - 1) % 3 != 0:
        return None, False
    if len({r % p for r in allroots}) != len(allroots):
        return None, False           # bad prime (root collision mod p)
    hc = SC.h_member(fr, gr)
    Ts = {k: SC.Tk(p, k, hc) for k in range(1, g + 1)}
    cpoly = SC.rail_charpoly(Ts, g)
    val = SC.validate_rail(cpoly, p, g)
    if not val["weil"]:
        return None, False
    return val["freeze"], True


def order_of_rootofunity(z, tol=1e-3):
    for o in (1, 2, 3, 4, 6):
        if abs(z ** o - 1) < tol:
            return o
    return None


