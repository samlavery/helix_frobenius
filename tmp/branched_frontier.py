"""BRANCHED FRONTIER PROBE -- can Schoen's etale-cover cycle extend to branched covers?

Frontier study for the OPEN abelian-sixfold Weil case: C: y^3 = f(x) g(x)^2 over P^1
(deg f = 4, deg g = 3; genus 6; K = Q(sqrt-3); signature (3,3)).  Schoen (1988/1998,
Patel-Zhang 2506.13729) proves ALGEBRAICITY of the exceptional (Weil) Hodge classes for
ETALE cyclic covers pi: C -> C', via the cycle  |K_{C'}| = P^{g'-1}  = the jumping fibre
of the base Abel-Jacobi map  AJ: Sym^{2g'-2}(C') -> J(C').  The branched cover has base
P^1 (g' = 0): |K_{P^1}| is empty (deg = -2), J(P^1) = 0.  This script does NOT try to
prove algebraicity; it MAPS THE GAP by testing whether the explicit branched-cover
candidate cycles land in the frozen collective Weil lane.

THE DISCRIMINATOR (the whole probe rests on this).  A cycle class defined over Q has a
Frobenius eigenvalue exactly p^{codim} at EVERY good prime (Tate over Q).  The Weil class
freeze scalar  r = det(Frob | V_zeta)/p^{g/2}  has ORDER = ord chi_3(D), D = disc(f)disc(g)^2
(the freeze-order law, item 27/37) -- so at the ~2/3 of primes with chi_3(D) != 1, r is a
NON-trivial cube root of unity: the Weil class is NOT Tate over Q there, hence provably
outside the Q-rational subalgebra generated by divisors + endomorphisms (all Tate over Q).
Any Q-rational candidate cycle (deck graphs, branch-point 0-cycles, theta-powers) whose
character is order 1 at every prime therefore CANNOT span the Weil lane.

Probes:
  1. EXCEPTIONALITY CERTIFICATE: measure ord(r) across primes for the non-CM fourfold and
     sixfold; confirm order-3 primes exist (non-Tate-over-Q).  Point-count validated vs the
     analytic freeze-order law.
  2. CANDIDATE KILLS: deck-graph / rail-projector, theta-power (Sym of the total space),
     branch-point 0-cycle, quotient-piece -- each shown order-1 (Tate over Q) => cannot be
     the order-3 Weil class.
  3. STRUCTURAL no-jump: the genus-0 base has NO special-divisor / jumping locus (Pic P^1
     = Z: every |O(d)| fills Sym^d(P^1)); Schoen's cycle is a positive-genus phenomenon.
  4. INSTRUMENT VALIDATION: a decomposable member IS divisor-reachable (freeze order 1,
     van Geemen) -- the discriminator is not vacuous; it would report a survivor.
  Falsifiers: root-scramble (arithmetic vs artifact), reading-scale (order-3 lane on w=3).

Run:  python3 branched_frontier.py [pmax]
"""
