def probe1_exceptionality(P, pmax):
    P("=" * 78)
    P("PROBE 1 -- EXCEPTIONALITY CERTIFICATE  (the discriminator)")
    P("=" * 78)
    P("The Weil freeze r = det(Frob|V_zeta)/p^{g/2}.  ord(r) = 3 at a prime  <=>  Frobenius")
    P("acts non-scalar on the 2-dim Weil space  <=>  the Weil class is NOT Tate over Q there")
    P("<=>  it is NOT in the Q-rational subalgebra generated by divisors/endomorphisms.")
    P("Measured (point counts) vs predicted (freeze-order law ord chi_3(disc f * disc g^2)).")
    P("")
    members = [
        ("FOURFOLD non-CM  y^3=f_2 g_3^2", [0, 1], [2, 3, 4], 4, pmax),
        ("SIXFOLD  non-CM  y^3=f_4 g_3^2", [0, 1, 2, 3], [4, 5, 6], 6, 14),
    ]
    for name, fr, gr, g, pm in members:
        D = poly_disc(fr) * poly_disc(gr) ** 2
        Dcf = cubefree_class(D)
        P(f"  {name}:  D = disc(f)*disc(g)^2 = {D} = {Dcf} (mod cubes){'  [nontrivial]' if Dcf != 1 else '  [trivial cube]'}")
        primes = [p for p in WS.sieve(pm) if p % 3 == 1 and p >= 7]
        orders_meas, orders_pred = [], []
        for p in primes:
            pred, _ = freeze_order_predicted(fr, gr, p)
            if pred == 0:
                continue
            r, ok = measure_freeze(fr, gr, g, p)
            if not ok:
                continue
            om = order_of_rootofunity(r)
            orders_meas.append((p, om))
            orders_pred.append((p, pred))
            tag = "MATCH" if om == pred else "**MISMATCH**"
            P(f"    p={p:>4d}   r = {r.real:+.4f}{r.imag:+.4f}i   ord(r) meas={om} pred={pred}  {tag}")
        got3 = any(o == 3 for _, o in orders_meas)
        P(f"  -> order-3 (non-Tate-over-Q) primes present: {got3}   "
          f"[{sum(1 for _,o in orders_meas if o==3)}/{len(orders_meas)} primes]")
        P(f"  -> CERTIFICATE: the Weil class of {name.split()[0]} is genuinely exceptional")
        P(f"     (outside the Q-rational divisor/endomorphism algebra).  Every Q-rational")
        P(f"     candidate cycle is Tate over Q (order 1 at all p) and is thereby EXCLUDED.")
        P("")


