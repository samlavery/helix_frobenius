def esym_2_4(leg_aps, ps):
    """e_2, e_4 of the degree-8 char poly of A = prod E_i, from the four a_p.

    Generating function  prod_i (1 + a_i x + p x^2);  e_k = coeff of x^k.
    Vectorised over primes.  leg_aps: list of four length-N arrays; ps: length-N.
    """
    N = len(ps)
    C = np.zeros((N, 9))
    C[:, 0] = 1.0
    for a in leg_aps:
        nC = np.array(C)                       # * 1
        nC[:, 1:] += C[:, :-1] * a[:, None]    # * a x
        nC[:, 2:] += C[:, :-2] * ps[:, None]   # * p x^2
        C = nC
    return C[:, 2], C[:, 4]


def brute_check(leg_aps, ps, nsample=6):
    """Verify e_2/p, e_4/p^2 against a direct eigenvalue computation.

    At each sample prime: build the eight eigenvalues (roots of each quadratic),
    sum products over all C(8,2) pairs / p and C(8,4) quadruples / p^2, compare
    to the elementary-symmetric formula.  Returns the max abs discrepancy.
    """
    e2, e4 = esym_2_4(leg_aps, ps)
    idx = np.linspace(0, len(ps) - 1, nsample).astype(int)
    worst = 0.0
    rows = []
    for j in idx:
        p = ps[j]
        eig = []
        for a in leg_aps:
            disc = complex(a[j] ** 2 - 4 * p)
            r = math.sqrt(p)
            s = np.sqrt(disc)
            eig += [(a[j] + s) / 2, (a[j] - s) / 2]
        eig = np.array(eig)
        pair = sum(eig[i] * eig[k] for i, k in itertools.combinations(range(8), 2))
        quad = sum(eig[i] * eig[k] * eig[l] * eig[m]
                   for i, k, l, m in itertools.combinations(range(8), 4))
        d2 = abs(pair / p - e2[j] / p)
        d4 = abs(quad / p ** 2 - e4[j] / p ** 2)
        worst = max(worst, d2, d4)
        rows.append((int(p), (e2[j] / p).real, d2, (e4[j] / p ** 2).real, d4))
    return worst, rows


