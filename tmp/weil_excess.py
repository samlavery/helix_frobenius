_PAIRS = list(itertools.combinations(range(8), 2))          # 28
_QUADS = list(itertools.combinations(range(8), 4))          # 70
_QIDX = {q: i for i, q in enumerate(_QUADS)}


def _perm_sign(seq):
    s, a = 1, list(seq)
    for i in range(len(a)):
        for j in range(i + 1, len(a)):
            if a[i] > a[j]:
                s = -s
    return s


# wedge tensor  W2222[q, a, b] = coeff of (pair_a ^ pair_b) in quad q
_W2222 = np.zeros((70, 28, 28))
for _a, (_i, _j) in enumerate(_PAIRS):
    for _b, (_k, _l) in enumerate(_PAIRS):
        if len({_i, _j, _k, _l}) == 4:
            _q = tuple(sorted((_i, _j, _k, _l)))
            _W2222[_QIDX[_q], _a, _b] += _perm_sign((_i, _j, _k, _l))


def _wedge2_matrix(M):
    """8x8 M -> 28x28 wedge^2 M  (2x2 minors)."""
    W = np.zeros((28, 28), dtype=complex)
    for a, (i, j) in enumerate(_PAIRS):
        for b, (k, l) in enumerate(_PAIRS):
            W[a, b] = M[i, k] * M[j, l] - M[i, l] * M[j, k]
    return W


def _companion_hat(a, p):
    sp = math.sqrt(p)
    return np.array([[a / sp, -sp], [1.0 / sp, 0.0]])       # eigenvalues e^{+-i th}


def _block_frobhat(leg_aps_at_p, p):
    M = np.zeros((8, 8))
    for i, a in enumerate(leg_aps_at_p):
        M[2 * i:2 * i + 2, 2 * i:2 * i + 2] = _companion_hat(a, p)
    return M


def divisor_generated(leg_aps, ps, nprimes=10, tol=1e-8):
    """Global NS = cap_p Eig_1(wedge^2 Fhat_p);  delta = rank of pairwise wedges.

    Uses a block-companion model (fixed integral basis) valid for products.
    Returns (rho_geom, delta).
    """
    # pick the first nprimes good primes (all legs defined, small p)
    sel = list(range(min(nprimes, len(ps))))
    blocks = []
    for j in sel:
        p = ps[j]
        M = _block_frobhat([a[j] for a in leg_aps], p)
        blocks.append(_wedge2_matrix(M) - np.eye(28))
    A = np.vstack(blocks)
    U, s, Vh = np.linalg.svd(A)
    r = int(np.sum(s > tol * s[0]))
    W = Vh[r:].conj().T                                    # 28 x rho
    rho = W.shape[1]
    cols = [np.einsum('qab,a,b->q', _W2222, W[:, a], W[:, b])
            for a in range(rho) for b in range(a, rho)]
    if not cols:
        return rho, 0
    Wq = np.array(cols).T
    sq = np.linalg.svd(Wq, compute_uv=False)
    delta = int(np.sum(sq > tol * sq[0])) if sq[0] > 0 else 0
    return rho, delta


