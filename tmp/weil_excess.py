def roots_esym_2_4(roots):
    """e_2, e_4 of eight Frobenius eigenvalues (roots), real parts."""
    c = np.poly(np.asarray(roots, dtype=complex))          # [1, -e1, e2, -e3, e4,...]
    return c[2].real, c[4].real


def roots_pos4_phases(roots, p):
    """The 4 'positive' unitarised phases of eight conjugate-paired roots.

    A real char poly gives roots in conjugate pairs e^{+-i phi_i} after dividing by
    sqrt(p); the four upper-half phases carry the object (the lower half is forced).
    """
    u = np.asarray(roots, dtype=complex) / math.sqrt(p)
    ph = np.sort(np.angle(u))
    return ph[4:8]                                          # the four positive phases


