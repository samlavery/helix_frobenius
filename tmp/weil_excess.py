def occupancy(leg_aps, ps):
    """rho = mean(e_2/p), tau = mean(e_4/p^2), each with standard error."""
    e2, e4 = esym_2_4(leg_aps, ps)
    a = e2 / ps
    b = e4 / (ps.astype(float) ** 2)
    return (a.mean(), a.std() / math.sqrt(len(ps)),
            b.mean(), b.std() / math.sqrt(len(ps)), len(ps))


