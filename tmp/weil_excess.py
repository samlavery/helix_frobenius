_SUB4 = list(itertools.combinations(range(8), 4))


def leg_angles(leg_aps, ps):
    """theta_i in [0, pi] per leg  (a_i = 2 sqrt(p) cos theta_i)."""
    return [np.arccos(np.clip(a / (2.0 * np.sqrt(ps)), -1.0, 1.0)) for a in leg_aps]


def occupancy_b_from_angles(th, w=1.0):
    """(2,2) occupancy from leg angles at winding w: mean sum_{4-subsets} cos(w*sum).

    Phases are +-theta_i per leg.  w = 1 reproduces tau (DC/eigenvalue-1 count on
    the mu6 lattice); w != 1 is an off-lattice reading.  Depends only on the angles,
    so it is Hasse-safe under scrambling (theta stays in [0, pi] under any permutation).
    """
    phs = np.stack(list(th) + [-t for t in th], axis=0)  # 8 x N
    tot = np.zeros(phs.shape[1])
    for S in _SUB4:
        tot += np.cos(w * sum(phs[m] for m in S))
    return tot.mean()


def occupancy_b_winding(leg_aps, ps, w):
    return occupancy_b_from_angles(leg_angles(leg_aps, ps), w)


