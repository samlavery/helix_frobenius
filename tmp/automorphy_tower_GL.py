import numpy as np


def vec_theta(Y, K=40):
    """theta(Y) = sum_{m in Z^n} exp(-pi m^T Y m),  Y positive-definite symmetric n x n."""
    n = Y.shape[0]
    rng = np.arange(-K, K + 1)
    grids = np.meshgrid(*([rng] * n), indexing="ij")
    M = np.stack([g.ravel() for g in grids], axis=0).astype(float)   # n x N
    q = np.einsum("in,ij,jn->n", M, Y, M)
    return np.sum(np.exp(-np.pi * q))


def rand_posdef(n, seed):
    rs = np.random.RandomState(seed)
    A = rs.randn(n, n)
    return A @ A.T + n * np.eye(n)      # well-conditioned SPD


def matrix_theta(n, K=6):
    """sum_{X in M_n(Z)} exp(-pi tr(X^T X)); f is its own matrix-Fourier transform, so Poisson
    says sum = sum_hat identically — we check the sum equals (1-dim theta)^{n^2} (separability)."""
    rng = np.arange(-K, K + 1)
    t1 = np.sum(np.exp(-np.pi * rng.astype(float) ** 2))    # 1-dim theta(1)
    return t1 ** (n * n), t1


def E(n, i, j):
    M = np.eye(n)
    M[i, j] = 1.0
    return M


