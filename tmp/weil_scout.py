def jacobi_coeffs(p, ind, N, i, j):
    """Exact Jacobi sum J(chi^i, chi^j) = sum_{t!=0,1} chi^i(t) chi^j(1-t) as an
       integer coefficient vector over {zeta_N^r}: returns coeffs[0..N-1]."""
    coeffs = [0] * N
    for t in range(2, p):
        r = (i * ind[t] + j * ind[(1 - t) % p]) % N
        coeffs[r] += 1
    return coeffs


def coeffs_value(coeffs, N):
    z = [cmath.exp(2j * math.pi * r / N) for r in range(N)]
    return sum(coeffs[r] * z[r] for r in range(N))


def charpoly_from_roots(roots):
    """monic poly prod (X - root) as coefficient list [c0..cn] (c_n=1), rounded."""
    poly = [1.0 + 0j]
    for r in roots:
        new = [0j] * (len(poly) + 1)
        for k, c in enumerate(poly):
            new[k] += c * (-r)
            new[k + 1] += c
        poly = new
    return poly


def Lpoly_from_roots(roots):
    """L-polynomial prod (1 - root T) = reversed char poly, coefficients rounded to
       nearest integer with the max rounding error reported."""
    # prod (1 - root T): coefficient of T^k = e_k(roots)*(-1)^k
    n = len(roots)
    coeff = [1.0 + 0j] + [0j] * n
    for r in roots:
        for k in range(n, 0, -1):
            coeff[k] += coeff[k - 1] * (-r)
    ints = [round(c.real) for c in coeff]
    err = max(abs(c.real - round(c.real)) + abs(c.imag) for c in coeff)
    return ints, err


def weil_factor_charpoly(p, a, b, N, verbose=False):
    """Degree-phi(N) Frobenius char poly of the Q(zeta_N) fourfold factor of
       Jac(y^N = x^a(1-x)^b) at a prime p = 1 mod N, via Jacobi sums.  Also returns
       validation metrics (purity, integrality, palindrome/FE, count reconstruction)."""
    assert (p - 1) % N == 0
    g = primitive_root(p)
    ind = index_table(p, g)
    U = units_mod(N)
    roots = []
    purity_dev = 0.0
    for n in U:
        cf = jacobi_coeffs(p, ind, N, (n * a) % N, (n * b) % N)
        J = coeffs_value(cf, N)
        purity_dev = max(purity_dev, abs(abs(J) ** 2 - p))
        roots.append(J)
    ints, integ_err = Lpoly_from_roots(roots)
    # functional equation: palindromic with weight-1 scaling: c_k * p^{deg-k?}...
    # For a weight-1 (abelian variety) L-poly of degree D=phi(N): c_{D-k} = p^{(D/2)-k} c_k?
    D = len(U)
    fe_ok = True
    for k in range(D + 1):
        lhs = ints[D - k]
        rhs = ints[k] * p ** (D // 2 - k) if (D // 2 - k) >= 0 else None
        if rhs is None:
            # c_k = c_{D-k} * p^{k-D/2}
            if ints[k] != ints[D - k] * p ** (k - D // 2):
                fe_ok = False
        else:
            if lhs != rhs:
                fe_ok = False
    # count reconstruction: full-curve affine count = p + sum_{j=1}^{N-1} J(chi^{ja},chi^{jb})
    full_sum = 0.0 + 0j
    for jj in range(1, N):
        ia, jb = (jj * a) % N, (jj * b) % N
        if ia == 0 or jb == 0 or ((jj * (a + b)) % N) == 0:
            # degenerate Jacobi sum -> value is -1 or character-sum edge; include exact
            cf = jacobi_coeffs(p, ind, N, ia, jb)
            full_sum += coeffs_value(cf, N)
        else:
            cf = jacobi_coeffs(p, ind, N, ia, jb)
            full_sum += coeffs_value(cf, N)
    predicted = p + full_sum
    brute = brute_affine_count(p, a, b, N)
    count_dev = abs(predicted.real - brute) + abs(predicted.imag)
    maxcoeff = max(abs(c) for c in ints) + 1
    # relative tolerances: the float residuals scale with machine-eps * magnitude,
    # so integrality / purity / count are judged against the object's own scale.
    integ_rel = integ_err / maxcoeff
    purity_rel = purity_dev / p
    count_rel = count_dev / (brute + 1)
