def ap_curve(p, a1, a2, a3, a4, a6):
    """a_p by direct count (short-model friendly)."""
    if p < 5:
        return None
    b2, b4, b6 = a1 * a1 + 4 * a2, 2 * a4 + a1 * a3, a3 * a3 + 4 * a6
    isq = bytearray(p)
    for x in range(p):
        isq[(x * x) % p] = 1
    s = 0
    for x in range(p):
        z = (4 * pow(x, 3, p) + b2 * x * x + 2 * b4 * x + b6) % p
        s += 0 if z == 0 else (1 if isq[z] else -1)
    return -s


def h4_tate_occupancy(alphas, p, tol=1e-6):
    """#{4-subsets S of the 8 H^1 Frobenius eigenvalues with prod = p^2} = number of
       Tate classes in H^4 = wedge^4 H^1 at the prime p (each is a (2,2) Hodge/Tate
       class candidate).  Exact-in-principle; here numeric with tolerance."""
    assert len(alphas) == 8
    cnt = 0
    tot = 0
    for S in itertools.combinations(range(8), 4):
        tot += 1
        prod = 1.0 + 0j
        for i in S:
            prod *= alphas[i]
        if abs(prod - p * p) < tol * p * p:
            cnt += 1
    return cnt, tot


def h4_tate_occupancy_list(roots, p, reltol=1e-4):
    """#{4-subsets S of the 8 H^1 Frobenius eigenvalues with prod = p^2} for a general
       list of 8 reciprocal roots (Jacobi sums); Tate class count in H^4 at p."""
    assert len(roots) == 8
    cnt = 0
    tot = 0
    for S in itertools.combinations(range(8), 4):
        tot += 1
        prod = 1.0 + 0j
        for i in S:
            prod *= roots[i]
        if abs(prod - p * p) < reltol * p * p:
            cnt += 1
    return cnt, tot


def cm_elliptic_alphas(p, ainv):
    """the 2 Frobenius eigenvalues (roots of X^2 - a_p X + p) for a good p."""
    ap = ap_curve(p, *ainv)
    if ap is None:
        return None
    disc = ap * ap - 4 * p
    if disc >= 0:
        r = math.isqrt(disc) if disc >= 0 else 0
        a1 = (ap + math.sqrt(disc)) / 2
        a2 = (ap - math.sqrt(disc)) / 2
        return [complex(a1), complex(a2)]
    s = math.sqrt(-disc)
    return [complex(ap / 2, s / 2), complex(ap / 2, -s / 2)]


