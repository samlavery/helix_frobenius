# PART 4 -- GENERAL RECIPE  (any genus-g curve -> degree-2g char poly; CM-agnostic)
#   the counting path for NON-CM / non-abelian-CM simple Weil targets, where Jacobi
#   sums do not apply.  #C(F_{p^k}), k=1..g, -> power sums -> char poly (Newton).
