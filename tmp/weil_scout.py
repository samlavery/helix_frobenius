def brute_affine_count(p, a, b, N):
    """#{(x,y) in F_p^2 : y^N = x^a (1-x)^b}, oracle-free ground truth."""
    powcount = [0] * p                      # powcount[c] = #{y : y^N = c}
    for y in range(p):
        powcount[pow(y, N, p)] += 1
    tot = 0
    for x in range(p):
        c = (pow(x, a, p) * pow((1 - x) % p, b, p)) % p
        tot += powcount[c]
    return tot


