def _kronecker(a, n):
    """Kronecker symbol (a/n)."""
    if n == 0:
        return 1 if a in (1, -1) else 0
    if n < 0:
        n = -n
    if a < 0:
        # (a/n) handled with sign below via (−1/n)
        pass
    # standard algorithm
    def ks(a, n):
        if n == 0:
            return 1 if abs(a) == 1 else 0
        if a % 2 == 0 and n % 2 == 0:
            return 0
        v = 0
        while n % 2 == 0:
            v += 1
            n //= 2
        if v % 2 == 0:
            k = 1
        else:
            k = 1 if a % 8 in (1, 7) else -1
        if n < 0:
            n = -n
            if a < 0:
                k = -k
        a %= n
        while a != 0:
            v = 0
            while a % 2 == 0:
                v += 1
                a //= 2
            if v % 2 == 1 and n % 8 not in (1, 7):
                k = -k
            if a % 4 == 3 and n % 4 == 3:
                k = -k
            a, n = n % a, a
        return k if n == 1 else 0
    return ks(a, n)


