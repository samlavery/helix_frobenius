def frac(x):
    """<x> fractional part of the rational x (a Fraction)."""
    return x - math.floor(x)


def eigdim(n, a, b, N):
    """dim of the chi^n eigenspace of H^{1,0} for y^N = x^a (1-x)^b.
       = <na/N> + <nb/N> + <nc/N> - 1,  c = -(a+b) mod N.  Returns 0 or 1, or
       None if any of na,nb,nc == 0 mod N (character not 'general' -- skip)."""
    c = (-(a + b)) % N
    if (n * a) % N == 0 or (n * b) % N == 0 or (n * c) % N == 0:
        return None
    s = frac(Fraction(n * a, N)) + frac(Fraction(n * b, N)) + frac(Fraction(n * c, N))
    d = int(s) - 1               # s is an integer (1 or 2); d in {0,1}
    return d


def cm_type(a, b, N):
    """Phi = {n in (Z/N)^* : eigdim(n)=1}; None if a character is non-general."""
    U = units_mod(N)
    Phi = []
    for n in U:
        d = eigdim(n, a, b, N)
        if d is None:
            return None
        if d == 1:
            Phi.append(n)
    return Phi


def is_primitive(Phi, N):
    """Phi primitive (=> the CM abelian variety is SIMPLE) iff its only
       multiplicative stabiliser in (Z/N)^* is 1."""
    S = set(Phi)
    for z in units_mod(N):
        if z == 1:
            continue
        if {(z * n) % N for n in Phi} == S:
            return False
    return True


def imaginary_quadratic_subgroups(N):
    """Index-2 subgroups H of (Z/N)^* with -1 not in H.  Each gives an IMAGINARY
       quadratic subfield K of Q(zeta_N); the two cosets are the two embeddings
       sigma, sigmabar of K.  Returns list of (label, H:set)."""
    U = units_mod(N)
    out = []
    seen = set()
    # index-2 subgroups = kernels of order-2 characters of the abelian group (Z/N)^*.
    # enumerate via: H = {u : u is a "square-like" wrt a quadratic character}.
    # brute: choose a candidate generating hyperplane by testing all subsets closed
    # under mult of index 2 is expensive; instead use the structure: order-2
    # characters <-> subfields.  We build them from Dirichlet-style quadratic symbols
    # over the prime-power / component structure by brute closure from generators.
    # Practical brute (U small here): find all subgroups of index 2.
    Uset = set(U)
    m = len(U)
    # A subgroup of index 2 is the kernel of a surjection U -> {+-1}. Enumerate such
    # homomorphisms by assigning signs to a generating set consistently.
    # Simple approach: try each u0 as "the" nontrivial value carrier is hard; instead
    # test every subset that is a subgroup of size m/2 by generating from pairs -- but
    # cleanest: characters. Build via: for each way to 2-color that is multiplicative.
    # We use: homs U->{+-1} <-> elements of U^/U^2 (dual). Compute U/U^2 basis.
    squares = {(u * u) % N for u in U}
    # coset reps of U/U^2
    reps, cover = [], set(squares)
    for u in U:
        if u not in cover:
            reps.append(u)
            cover |= {(u * r) % N for r in cover} | {(u * s) % N for s in squares}
            # extend cover as subgroup generated
            changed = True
            while changed:
                changed = False
                new = {(x * y) % N for x in cover for y in cover}
                if not new <= cover:
                    cover |= new
                    changed = True
    # characters: sign on each rep in {+-1}; full char determined by signs on U/U^2.
    r = len(reps)
    for bits in range(1, 1 << r):           # nontrivial characters
        sign = {}

        def chi(u):
            # factor u into squares * product of reps^{e}; sign = prod of rep signs^e
            # compute by: write membership via reps chosen greedily
            val = 1
            uu = u
            # reduce modulo squares by trial over rep products
            for k in range(r):
                if (bits >> k) & 1:
                    pass
            return val
        # The greedy factorization above is unreliable; fall back to direct kernel
        # construction below.
        break
    # -------- robust fallback: enumerate index-2 subgroups directly --------
    out = []
    subgroups = _index2_subgroups(U, N)
    idx = 0
    for H in subgroups:
        if (N - 1) % N not in H and (N - 1) not in H:   # -1 not in H  => imaginary
            idx += 1
            out.append((f"K{idx}", H))
    return out


def _index2_subgroups(U, N):
    """All index-2 subgroups of (Z/N)^* (small N)."""
    Uset = set(U)
    m = len(U)
    res = []
    seen = set()
    # order-2 quadratic characters via all sign assignments consistent with mult:
    # build the group table, find subgroups of size m/2 that are closed.
    # Since m is small (<=16 here) do it via: a subgroup of index 2 contains all
    # squares; U/U^2 is elementary abelian 2-group; index-2 subgroups <-> hyperplanes.
    squares = set()
    for u in U:
        squares.add((u * u) % N)
    # generate the subgroup <squares>
    sq = set(squares)
    changed = True
    while changed:
        changed = False
        add = {(x * y) % N for x in sq for y in sq}
        if not add <= sq:
            sq |= add
            changed = True
    sq.add(1)
    # coset representatives of U / sq
    cosets = []
    used = set()
    for u in U:
        if u not in used:
            c = {(u * s) % N for s in sq}
            cosets.append(u)
            used |= c
    reps = cosets                    # includes rep 1 (for sq itself)
    reps_nontriv = [x for x in reps if x % N != 1]
    k = len(reps_nontriv)
    # each hyperplane = kernel of a nonzero linear functional on (Z/2)^k
    # map coset -> vector: express rep as product of chosen basis reps? Instead,
    # index cosets 0..2^k-1 by a fixed order and treat reps_nontriv as basis only if
    # independent.  For small groups we just enumerate index-2 subgroups by brute:
    from itertools import combinations
    # brute: any subset of U of size m//2 that contains 1, is closed under mult and
    # inverse, and closed under mult by sq.
    target = m // 2
    # build candidate subgroups as sq extended by a subset of reps_nontriv of the
    # right corank; easier: for each nontrivial character chi: U->{+-1}, ker.
    # We construct chi by assigning +-1 to each nontrivial coset rep freely, then
    # extending multiplicatively via coset arithmetic.
    # coset multiplication table:
    def coset_of(u):
        for i, rp in enumerate(reps):
            if (u * pow(rp, -1, N)) % N in sq:
                return i
        return None
    cmul = {}
    for i, ri in enumerate(reps):
        for j, rj in enumerate(reps):
            cmul[(i, j)] = coset_of((ri * rj) % N)
    nc = len(reps)
    for signs_bits in range(1, 1 << nc):
        sign = [1] * nc
        for i in range(nc):
            sign[i] = 1 if not ((signs_bits >> i) & 1) else -1
        # check multiplicativity: sign[cmul[i,j]] == sign[i]*sign[j]
        ok = True
        for i in range(nc):
            for j in range(nc):
                if sign[cmul[(i, j)]] != sign[i] * sign[j]:
                    ok = False
                    break
            if not ok:
                break
        if not ok:
            continue
        # sign(1)=+1 required
        i1 = coset_of(1)
        if sign[i1] != 1:
            continue
        H = frozenset(u for u in U if sign[coset_of(u)] == 1)
        if len(H) == target and H not in seen:
            seen.add(H)
            res.append(set(H))
    return res


def signature(Phi, H, N):
    """(#Phi in H, #Phi not in H) = the K-signature of the CM type."""
    ins = sum(1 for n in Phi if n in H)
    return (ins, len(Phi) - ins)


def cyclic_cover_signature(exps, n):
    """mu_n eigenspace signature of H^{1,0} for a cyclic degree-n cover with FINITE
       branch-point local exponents `exps` (list of a_i in 1..n-1); infinity carries
       exponent (-sum exps) mod n if nonzero.  Chevalley-Weil:
       dim(H^{1,0})_{chi^j} = -1 + sum_over_all_branch <j*a/n>.  Returns
       (dims dict j->dim, genus, e_infinity)."""
    e_inf = (-sum(exps)) % n
    allexp = exps + ([e_inf] if e_inf != 0 else [])
    dims = {}
    for j in range(1, n):
        d = -1 + sum(frac(Fraction(j * e, n)) for e in allexp)
        dims[j] = int(d)
    return dims, sum(dims.values()), e_inf


def weil_cover_configs(n, g, Kdim_each):
    """branch configurations (r1 exponent-1 points, r2 exponent-2 points) of a cyclic
       degree-n cover whose Jacobian is a balanced Weil-type g-fold: all mu_n
       eigenspaces of H^{1,0} equal `Kdim_each`.  Returns (r1, r2, e_inf) triples."""
    out = []
    for r1 in range(0, 3 * g + 3):
        for r2 in range(0, 3 * g + 3):
            exps = [1] * r1 + [2] * r2
            if not exps:
                continue
            dims, gg, ei = cyclic_cover_signature(exps, n)
            if gg == g and len(set(dims.values())) == 1 and dims[1] == Kdim_each:
                out.append((r1, r2, ei))
    return out


def rail_freeze(Jn, units, p, wedge, Kmod=3, Kres=1):
    """the K-eigen RAIL reading.  W = eigenlines n with n%Kmod==Kres (one K embedding).
       Returns ((wedge^dimW W)/p^wedge, W-rail units): the top-rail Frobenius scalar
       normalised by p^{g/2}.  = 1 exactly <=> the Weil class FREEZES (Tate class over
       F_p, trivial Hecke character); a nontrivial phase = the K-Hecke character value
       (no freeze).  wedge = dim of the rail = g/2."""
    Wr = [n for n in units if n % Kmod == Kres]
    prod = 1.0 + 0j
    for n in Wr:
        prod *= Jn[n]
    return prod / p ** wedge, Wr


def scan_weil_covers(Nlist):
    """Portfolio PART 1: over cyclic covers y^N=x^a(1-x)^b (phi(N)=8 gives dim-4
       factors), classify every realizable degree-8 CM type of the Q(zeta_N) factor
       by (primitive?=simple) x (balanced (2,2)?=Weil type).  Returns the full list;
       the caller reports the landscape.  KEY EMPIRICAL FACT (checked here and by an
       exhaustive CM-type enumeration): for cyclotomic (abelian) E of degree 8 the
       SIMPLE (primitive) types are all (1,3)/(3,1) [exceptional-Hodge, Shioda flavor]
       and the balanced (2,2) [Weil type] types are all IMPRIMITIVE [non-simple,
       reduce to products -> algebraic].  A SIMPLE balanced-(2,2) Weil fourfold needs
       a NON-abelian degree-8 CM field (Mumford's twist) -- not realizable by a cover."""
    rows = []
    for N in Nlist:
        if len(units_mod(N)) != 8:        # want a degree-8 CM field -> dim-4 factor
            continue
        Ksub = imaginary_quadratic_subgroups(N)
        Kfields = _name_quadratic_fields(N, Ksub)
        seen_phi = set()
        for a in range(1, N):
            for b in range(1, N):
                if math.gcd(a, N) != 1 or math.gcd(b, N) != 1:
                    continue
                if (a + b) % N == 0:
                    continue
                Phi = cm_type(a, b, N)
                if Phi is None or len(Phi) != 4:
                    continue
                fphi = frozenset(Phi)
                if fphi in seen_phi:
                    continue
                seen_phi.add(fphi)
                prim = is_primitive(Phi, N)
                sigs = [(fld, signature(Phi, H, N))
                        for (lbl, H), fld in zip(Ksub, Kfields)]
                balanced = [(fld, sg) for fld, sg in sigs if sg == (2, 2)]
                rows.append(dict(N=N, a=a, b=b, Phi=sorted(Phi),
                                 primitive=prim, sigs=sigs, balanced=balanced))
    return rows


def exhaustive_primitive_balanced(Nlist):
    """Enumerate ALL CM types (not just cover-realizable) of each Q(zeta_N) and count
       those that are simultaneously primitive AND balanced-(2,2) over some imaginary
       quadratic K.  Returns {N: count}.  (Establishes the no-simple-Weil obstruction
       for cyclotomic CM fields.)"""
    def conj_pairs(N):
        U = units_mod(N)
        seen, pairs = set(), []
        for n in U:
            if n in seen:
                continue
            m = (N - n) % N
            seen |= {n, m}
            pairs.append((n, m))
        return pairs
    res = {}
    for N in Nlist:
        if len(units_mod(N)) != 8:
            continue
        Ksub = imaginary_quadratic_subgroups(N)
        cnt = 0
        for choice in itertools.product(*[(p[0], p[1]) for p in conj_pairs(N)]):
            Phi = list(choice)
            if not is_primitive(Phi, N):
                continue
            if any(signature(Phi, H, N) == (2, 2) for _, H in Ksub):
                cnt += 1
        res[N] = cnt
    return res


def _name_quadratic_fields(N, Ksub):
    """Best-effort name for each imaginary quadratic subfield of Q(zeta_N) by
       matching its ramification (conductor) to sqrt(-d).  Returns list of strings
       aligned with Ksub."""
    names = []
    for lbl, H in Ksub:
        # the subfield's conductor divides N; identify by the quadratic character's
        # modulus.  We report the fundamental discriminant guess from H via the set
        # of primes ramifying (those p|N where H does not contain the full residue).
        names.append(_qfield_name(N, H))
    return names


def _qfield_name(N, H):
    """Identify Q(sqrt(-d)) for the index-2 subgroup H <= (Z/N)^* (imaginary)."""
    # The quadratic character chi_H mod N has some conductor f; the field is
    # Q(sqrt(d*)) with d* the fundamental discriminant of conductor f and sign
    # negative (imaginary since -1 not in H).  Match against small candidates.
    U = units_mod(N)
    def chiH(u): return 1 if (u % N) in H else -1
    for d in [1, 2, 3, 5, 6, 7, 10, 11, 13, 14, 15, 19, 21, 22, 23, 30, 33, 35]:
        disc = -d if d % 4 == 3 else -4 * d
        # candidate character chi_disc(u) = Kronecker(disc, u); compare on U
        ok = True
        matched_any = False
        for u in U:
            k = _kronecker(disc, u)
            if k == 0:
                continue
            matched_any = True
            if k != chiH(u):
                ok = False
                break
        if ok and matched_any:
            return f"Q(sqrt{disc//abs(_squarefree_sign(disc))}) [disc {disc}]" \
                   .replace("sqrt", "√")
    return "imag.quad.(unidentified)"


