def _prime_factors(n):
    f, d = [], 2
    while d * d <= n:
        while n % d == 0:
            f.append(d)
            n //= d
        d += 1
    if n > 1:
        f.append(n)
    return f


def _norm(a, p):
    a = [x % p for x in a]
    while len(a) > 1 and a[-1] == 0:
        a.pop()
    return a


def _padd(a, b, p):
    n = max(len(a), len(b))
    return _norm([(a[i] if i < len(a) else 0) + (b[i] if i < len(b) else 0)
                  for i in range(n)], p)


def _psub(a, b, p):
    n = max(len(a), len(b))
    return _norm([(a[i] if i < len(a) else 0) - (b[i] if i < len(b) else 0)
                  for i in range(n)], p)


def _pmulr(a, b, p):
    """full polynomial product over F_p (no reduction)."""
    r = [0] * (len(a) + len(b) - 1)
    for i, ai in enumerate(a):
        if ai:
            for j, bj in enumerate(b):
                r[i + j] = (r[i + j] + ai * bj) % p
    return _norm(r, p)


def _pdivmod(a, b, p):
    """(q, r) with a = q*b + r, deg r < deg b, over F_p."""
    a = _norm(a, p)
    b = _norm(b, p)
    db = len(b) - 1
    binv = pow(b[-1], p - 2, p)
    r = a[:]
    q = [0] * max(1, len(a) - db)
    while len(r) - 1 >= db and r != [0]:
        c = (r[-1] * binv) % p
        sh = len(r) - 1 - db
        q[sh] = c
        for i in range(len(b)):
            r[i + sh] = (r[i + sh] - c * b[i]) % p
        r = _norm(r, p)
    return _norm(q, p), _norm(r, p)


def _pmodm(a, m, p):
    return _pdivmod(a, m, p)[1]


def _pmul(a, b, m, p):
    return _pmodm(_pmulr(a, b, p), m, p)


def _ppow(a, e, m, p):
    r = [1]
    a = _pmodm(a, m, p)
    while e:
        if e & 1:
            r = _pmul(r, a, m, p)
        a = _pmul(a, a, m, p)
        e >>= 1
    return r


def _pgcd(a, b, p):
    a = _norm(a, p)
    b = _norm(b, p)
    while b != [0]:
        a, b = b, _pmodm(a, b, p)
    if a != [0] and a[-1] != 1:
        inv = pow(a[-1], p - 2, p)
        a = _norm([(x * inv) % p for x in a], p)
    return a


def find_irreducible(p, k):
    """a monic irreducible poly of degree k over F_p (coeff list low->high), via the
       Rabin test: m irreducible <=> x^{p^k} = x mod m and gcd(x^{p^{k/q}}-x, m)=1
       for every prime q | k."""
    import random
    if k == 1:
        return [0, 1]                           # F_p itself (t = 0)
    x = [0, 1]
    while True:
        m = _norm([random.randrange(p) for _ in range(k)] + [1], p)
        if len(m) != k + 1:
            continue
        xr = _pmodm(x, m, p)                     # x reduced (= x for k>=2)
        if _psub(_ppow(x, p ** k, m, p), xr, p) != [0]:
            continue
        ok = True
        for q in set(_prime_factors(k)):
            xpd = _ppow(x, p ** (k // q), m, p)
            if _pgcd(_psub(xpd, xr, p), m, p) != [1]:
                ok = False
                break
        if ok:
            return m


def count_hyperelliptic_ext(fcoeffs, p, k, m):
    """#C(F_{p^k}) for C: y^2 = f(x), f = integer coeff list (deg odd, one point at
       infinity); m = irreducible of degree k defining F_{p^k}.  Oracle-free count."""
    q = p ** k
    exp = (q - 1) // 2
    fred = [fi % p for fi in fcoeffs]

    def val_at(xc):
        acc = [0]                               # Horner: f(x) in F_{p^k}
        for fi in reversed(fred):
            acc = _pmul(acc, xc, m, p)
            acc = _padd(acc, [fi], p)
        return acc

    cnt_affine = 0
    for idx in range(q):
        xc, t = [], idx
        for _ in range(k):
            xc.append(t % p)
            t //= p
        fx = val_at(_norm(xc, p))
        if fx == [0]:
            cnt_affine += 1                      # y = 0 (single point)
        elif _ppow(fx, exp, m, p) == [1]:
            cnt_affine += 2                      # square -> two y
        # nonsquare -> no points
    return cnt_affine + 1                        # + point at infinity


def charpoly_from_pointcounts(counts, p, g):
    """counts[k] = #C(F_{p^k}) for k=1..g.  Returns the degree-2g Frobenius L-poly
       coefficients (integers) via power sums + Newton + functional equation."""
    s = [0.0] * (g + 1)
    for k in range(1, g + 1):
        s[k] = p ** k + 1 - counts[k]           # s_k = sum alpha_i^k
    # Newton: e_k from s_1..s_k  (e_0=1)
    e = [0.0] * (g + 1)
    e[0] = 1.0
    for k in range(1, g + 1):
        acc = 0.0
        for i in range(1, k + 1):
            acc += (-1) ** (i - 1) * e[k - i] * s[i]
        e[k] = acc / k
    # P(T) = prod(1 - alpha_i T) = sum_{k=0}^{2g} c_k T^k, c_k = (-1)^k e_k for k<=g,
    # and c_{2g-k} = p^{g-k} c_k (functional equation).
    c = [0] * (2 * g + 1)
    for k in range(0, g + 1):
        c[k] = round((-1) ** k * e[k])
    for k in range(0, g):
        c[2 * g - k] = c[k] * p ** (g - k)
    return c


