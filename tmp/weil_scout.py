def sieve(n):
    s = bytearray([1]) * (n + 1)
    s[0] = s[1] = 0
    for i in range(2, int(n ** 0.5) + 1):
        if s[i]:
            s[i * i::i] = bytearray(len(range(i * i, n + 1, i)))
    return [i for i in range(n + 1) if s[i]]


def primitive_root(p):
    fac, n, d = [], p - 1, 2
    while d * d <= n:
        if n % d == 0:
            fac.append(d)
            while n % d == 0:
                n //= d
        d += 1
    if n > 1:
        fac.append(n)
    for g in range(2, p):
        if all(pow(g, (p - 1) // q, p) != 1 for q in fac):
            return g
    raise RuntimeError


def index_table(p, g):
    """ind[t] = discrete log of t base g (ind[0] left 0, never used)."""
    ind = [0] * p
    v = 1
    for k in range(p - 1):
        ind[v] = k
        v = (v * g) % p
    return ind


def units_mod(N):
    return [a for a in range(1, N) if math.gcd(a, N) == 1]


