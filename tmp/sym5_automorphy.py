import numpy as np


def theta(Y, K):
    n = Y.shape[0]
    rng = np.arange(-K, K + 1)
    grids = np.meshgrid(*([rng] * n), indexing="ij")
    M = np.stack([g.ravel() for g in grids], axis=0).astype(np.float64)
    q = np.einsum("in,ij,jn->n", M, Y, M, optimize=True)
    return float(np.sum(np.exp(-np.pi * q)))


def near_identity_spd(n, seed):
    rs = np.random.RandomState(seed)
    Q, _ = np.linalg.qr(rs.randn(n, n))           # random orthogonal
    d = 0.6 + 1.0 * rs.rand(n)                     # eigenvalues in [0.6, 1.6]
    return (Q * d) @ Q.T


def E(n, i, j):
    M = np.eye(n)
    M[i, j] = 1.0
    return M


